// Add an event listener to the button
document.getElementById('sound1').addEventListener('click', () => {
    setTimeout(() => {
        // Open the Spotify link in a new tab after a small delay
        window.open('https://open.spotify.com/track/63mdJr3NMY3wReOkNE6c6W?si=a65c563054ae412c', '_blank');
    }, 500); // Delay to let the sound play before navigating
});

